            <h2>EDITAL 04/2015 da Pró-Reitoria de Pesquisa e Pós-Graduação e Agência PUC de Ciência, Tecnologia e Inovação - CONCURSO PUC JOVENS IDEIAS</h2>
            
            <p>A Pró-Reitoria de Pesquisa e Pós-Graduação da PUCPR (PRPPG) e a Agência PUC de Ciência, Tecnologia e Inovação divulgam à comunidade científica o regulamento do Concurso PUC JOVENS IDEIAS. </p>
            <br />
            <h4>I - DISPOSIÇÕES PRELIMINARES</h4>

            <p>Artigo 1º - Este regulamento estabelece as bases administrativas, jurídicas e técnicas para inscrição, participação, seleção e premiação do Concurso, doravante denominado PUC Jovens Ideias, com sede e foro na Cidade de Curitiba, Estado do Paraná.</p>
            <br />
            <h4>II - CARACTERIZAÇÃO</h4>
            
            <p>Artigo 2º - O concurso PUC Jovens Ideias é promoção da Pró-Reitoria de Pesquisa e Pós-Graduação da PUCPR e da Agência PUC de Ciência, Tecnologia e Inovação.</p>
            
            <p>Artigo 3º - Este concurso tem como objetivos:</p>
                <p style="margin-left: 10%;">a)	Atrair estudantes de ensino médio, de graduação e de pós-graduação inventivos e empreendedores dispostos a compartilhar suas ideias com a comunidade acadêmica, dando abertura para a criatividade, a inovação e o fator empreendedor da proposta; </p>
                <p style="margin-left: 10%;">b)	Identificar e incentivar estudantes de ensino médio, de graduação e de pós-graduação que apresentam perfil inventivo, em busca de projetos com potencial para se transformar em Inovação.</p>
            <br />
            <h4>III - PARTICIPAÇÃO</h4>
            
            <p>Artigo 4º - A participação no concurso PUC Jovens ideais é aberta a:</p>
<p style="margin-left: 10%;">a)	estudantes de graduação e pós-graduação regularmente matriculados na PUCPR de todos os <i>campi</i>;</p>
<p style="margin-left: 10%;">b)	estudantes de graduação e pós-graduação de outras IES brasileiras. </p>
<p style="margin-left: 10%;">c)	estudantes de ensino médio dos Colégios que participam do Programa PIBIC Jr da PUCPR situados em Curitiba e Região Metropolitana (anexo 1);</p>
            
            <p>Artigo 5º - A participação poderá ser individual ou em grupo de até 4 estudantes e um professor, se for o caso.</p>
            
            <br />
            <h4>IV - MODALIDADES</h4>
            
            <p>Artigo 6º - As propostas a serem inscritas deverão respeitar as modalidades abaixo:</p>

            <p style="margin-left: 10%;">a)	Jovens Ideias - esta modalidade está restrita a propostas oriundas de estudantes sem a orientação de professor;</p>
            <p style="margin-left: 10%;">b)	Pesquisar é Evoluir - nesta modalidade podem ser inscritas propostas decorrentes de projetos de iniciação científica (PIBIC), iniciação tecnológica (PIBITI), trabalho de conclusão de curso (TCC), dissertações de mestrado, tese de doutorado. Desse modo, configuram-se propostas que possuem um professor orientador.</p>
            <p style="margin-left: 10%;">c)	Jovens Ideias Júnior - nesta modalidade podem ser inscritas propostas oriundas de estudantes de ensino médio, com ou sem orientação de professor.</p>

            <h4>V - INSCRIÇÃO</h4>
            
            <p>Artigo 7º - A inscrição deverá ser realizada no site www.pucpr.br/jovensideias2015 no período de 30/05 a 27/07/2015, até as 23:59h, enviando os seguintes documentos:</p> 

            <p>a) O projeto contendo (arquivo em pdf, mínimo de 04 e máximo 10 páginas com até 20MB):</p>

            <ul>
                <li>- Folha de rosto: título do projeto, nome(s) do(s) estudante(s), período(s), curso(s), escola(s)/faculdade(s), IES. E-mail(s), fone(s).</li>
                <li>- O quê? (contextualização, proposta do projeto)</li>
                <li>- Para quem? (público-alvo)</li>
                <li>- Quais benefícios? (impactos da proposta)</li>
                <li>- Detalhamento Técnico</li>
                <li>- Viabilidade do Negócio</li>
                <li>- Anexo com imagens / ilustrações (se for o caso)</li>
            </ul>

            <p>b) Link do vídeo com até 2 minutos mostrando o estágio de desenvolvimento do protótipo publicado no Youtube ou outro website similar de compartilhamento de vídeo;</p>

            <p>c) Comprovante oficial de matrícula de todos os membros do grupo em PDF (em papel timbrado da IES ou Colégio e com assinatura).</p>

            <p>Parágrafo Único - Ao se inscrever neste concurso, o candidato ou grupo, garantem que o projeto é resultado de seu trabalho e autoria, não configurando plágio nem violação a qualquer direito de propriedade intelectual de terceiros, eximindo a PUCPR de qualquer responsabilidade decorrente de falsidade desta declaração, responsabilizando-se o autor por eventuais lesões/prejuízos a direito de terceiros.</p>

            <h4>VI - SELEÇÃO</h4>

            <p>Artigo 8º - Serão selecionados os 10 (dez) melhores trabalhos da modalidade 'a' e 'b' mencionadas no artigo 6º; e 05 (cinco) melhores trabalhos da modalidade 'c' do mesmo artigo.</p>

            <h4>VII - EXPOSIÇÃO</h4>

            <p>Artigo 9º - Os trabalhos selecionados serão apresentados à comunidade acadêmica durante o XXIII SEMIC (Seminário de Iniciação Científica) a ser realizado nos dias 6, 7 e 8 de outubro de 2015, campus Curitiba.</p>

            <p>Artigo 10º - A participação do(s) estudante(s) das propostas selecionadas na exposição durante XXIII SEMIC é obrigatória, sob pena de desclassificação.</p>

            <p>Parágrafo Único - A critério dos participantes e da Instituição, a apresentação pública dos projetos poderá ser substituída por avaliação em apresentação para banca fechada para fins de proteção da novidade da propriedade intelectual.</p>

            <h4>VIII - CRITÉRIOS DE AVALIAÇÃO</h4>

            <p>Artigo 11. - O projeto com característica de inovação pode ser um serviço, processo ou produto, comprometido com o desenvolvimento da sociedade.</p>

            <p>Artigo 12. - Os projetos serão avaliados, tanto na pré-seleção como na exposição durante o XXIII SEMIC, levando-se em consideração: originalidade, criatividade, impactos na solução de problemas de um nicho específico, viabilidade técnica, potencial para implementação, qualidade da apresentação do projeto.<p>


            <h4>IX - COMISSÃO AVALIADORA</h4>
            <p>02 Representantes da Agência PUC de Ciência, Tecnologia e Inovação<br />
            01 Representante da Iniciação Científica<br />
            01 Representante da PRPPG<br />
            01 Representante de cada Escola da PUCPR (Escola de Direito, Escola Politécnica, etc.)<br />
            02 Representantes da Diretoria de Relacionamento</p>

            <h4>X - PREMIAÇÃO</h4>

            <p>Artigo 13. - As duas melhores propostas da modalidade 'a' e 'b' serão premiadas: 1º lugar: R$1.000,00 (hum mil real); 2º Lugar: Menção Honrosa. As duas melhores propostas do ensino médio serão premiadas: 1º Lugar: vale-presente no valor de R$ 150,00; 2º Lugar: Menção Honrosa.</p>

            <p>Parágrafo 1º - Todas as propostas premiadas serão analisadas pela Agência PUC de Ciência, Tecnologia e Inovação quanto à viabilidade de receberem um Curso de Mentoria de acordo com o nível de desenvolvimento e maturidade da proposta.</p>

            <p>Parágrafo 2º - O valor em dinheiro será divido por todos os membros do grupo.</p>

            <p>Parágrafo 3º - Para as propostas do ensino médio, cada participante ganhará um vale-presente livro de R$150,00.</p>

            <p>Artigo 14. - As decisões da comissão avaliadora são irrecorríveis.</p>

            <h4>XI - DIREITOS AUTORAIS</h4>

            <p>Artigo 15. - A Agência PUC de Ciência, Tecnologia e Inovação orientará os autores das 25 propostas selecionadas quanto à questão de propriedade intelectual.</p>

            <p>Artigo 16. - Os projetos selecionados que resultarem na produção de patente de invenção, modelo de utilidade e desenho industrial, bem como aqueles que resultarem em programas de computador, desde que desenvolvidos exclusivamente por alunos da PUCPR, serão de titularidade da Associação Paranaense de Cultura-APC, sendo que os ganhos econômicos resultantes da exploração comercial de resultado de criação intelectual serão distribuídos entre a APC e os desenvolvedores, na forma do artigo 4º da Portaria nº.31/2006.</p>

